"use strict";
// Q=36: T-Shirt: Write a function called make_shirt() that accepts a size and the text of a message that should be printed on the shirt.
// The function should print a sentence summarizing the size of the shirt and the message printed on it. 
// Call the function.
function make_shirt(size, text) {
    console.log(`${size}, T-shirt with message: ${text}`);
}
make_shirt('Large', 'I love programming');
make_shirt('Medium', 'Make it work, make it right, make it fast');
make_shirt('Small', 'Code is like humor.');
